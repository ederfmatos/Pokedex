function generateKey(t1, t2) {
  return `${t1}_${t2}`;
}

function getPairs() {
  let pairs = [];
  data.forEach((row, first) => {
    row.forEach((multiply, second) => {
      pairs[generateKey(types[first], types[second])] = multiply;
    });
  });
  return pairs;
}

const table = getPairs();

function matchupTypes(primary, secondary, comparator) {
  debugger;
  const x1 = table[generateKey(comparator, primary)];
  const x2 =
    primary !== secondary && secondary !== 'none'
      ? table[generateKey(comparator, secondary)]
      : 1;

  const value = x1 * x2;

  if (
    value === 4 ||
    value === 2 ||
    value === 1 ||
    value == 0.5 ||
    value == 0.25 ||
    value == 0
  )
    return value;

  return 'Invalid types';
}

export default function DefensiveMatchups(primary, secondary = 'none') {
  return types.map(type => ({
    type,
    effective: matchupTypes(primary, secondary, type)
  }));
}
